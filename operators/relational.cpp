#include<iostream>
using namespace std;

int main(){
    //RELATIONAL OPERATOR -> gives true or false
    int a=10;
    int b=5;

    cout<<(a>b)<<endl;            //10>5 -> true (1)
    cout<<(a<b)<<endl;            //10<5 -> false (0)
    cout<<(a<=b)<<endl;           //10<=5 -> false(0)
    cout<<(a>=b)<<endl;           //10>=5 -> true(1)
    cout<<(a==b)<<endl;           //10==5 -> false(0)
    cout<<(a!=b)<<endl;           //10!=5 -> true(1)



    return 0;

}